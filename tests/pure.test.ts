describe('validateProxyUrl', () => {
  test('accepts http, https, socks4, socks5', () => {
    expect(validateProxyUrl('http://host:8080')).toBe(true);
    expect(validateProxyUrl('https://host:8080')).toBe(true);
    expect(validateProxyUrl('socks4://host:1080')).toBe(true);
    expect(validateProxyUrl('socks5://user:pass@host:1080')).toBe(true);
  });

  test('rejects unsupported schemes', () => {
    expect(validateProxyUrl('ftp://host')).toBe(false);
  });

  test('rejects invalid URLs', () => {
    expect(validateProxyUrl('not-a-url')).toBe(false);
    expect(validateProxyUrl('')).toBe(false);
  });
});

describe('formatTranscriptForCli', () => {
  test('json format returns pretty-printed transcript', () => {
    const out = formatTranscriptForCli(sampleTranscript, 'json', false);
    expect(JSON.parse(out).videoId).toBe('abc12345678');
  });

  test('srt format produces SRT', () => {
    expect(formatTranscriptForCli(sampleTranscript, 'srt', false)).toContain('00:00:00,000');
  });

  test('vtt format starts with WEBVTT', () => {
    expect(formatTranscriptForCli(sampleTranscript, 'vtt', false)).toStartWith('WEBVTT');
  });

  test('default (text) honours includeTimestamps flag', () => {
    expect(formatTranscriptForCli(sampleTranscript, 'text', false)).toBe('Hello world');
    expect(formatTranscriptForCli(sampleTranscript, 'text', true)).toContain('[0:00] Hello world');
  });
});

describe('formatTranscriptForMcp', () => {
  test('segments format returns structured JSON', () => {
    const out = formatTranscriptForMcp(sampleTranscript, 'segments', false);
    const parsed = JSON.parse(out);
    expect(parsed.videoId).toBe('abc12345678');
    expect(parsed.segments).toHaveLength(2);
  });

  test('text format returns plain text', () => {
    expect(formatTranscriptForMcp(sampleTranscript, 'text', false)).toBe('Hello world');
  });

  test('srt/vtt round-trip', () => {
    expect(formatTranscriptForMcp(sampleTranscript, 'srt', false)).toContain('00:00:00,000');
    expect(formatTranscriptForMcp(sampleTranscript, 'vtt', false)).toStartWith('WEBVTT');
  });
});
