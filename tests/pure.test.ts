describe('extractApiKey', () => {
  test('extracts API key from inline JSON config', () => {
    const html = '<script>"INNERTUBE_API_KEY":"AIzaSyABC123"</script>';
    expect(extractApiKey(html)).toBe('AIzaSyABC123');
  });

  test('tolerates whitespace around the colon', () => {
    const html = '"INNERTUBE_API_KEY"   :   "AIzaSyXYZ"';
    expect(extractApiKey(html)).toBe('AIzaSyXYZ');
  });

  test('returns null when key not present', () => {
    expect(extractApiKey('<html></html>')).toBeNull();
  });
});

describe('extractTracksFromHTML', () => {
  test('extracts captionTracks from ytInitialPlayerResponse', () => {
    const playerResponse = {
      captions: {
        playerCaptionsTracklistRenderer: {
          captionTracks: [
            { baseUrl: 'https://example.com/en', languageCode: 'en' },
          ],
        },
      },
    };
    const html = `<script>var ytInitialPlayerResponse = ${JSON.stringify(playerResponse)};</script>`;
    const tracks = extractTracksFromHTML(html);
    expect(tracks).not.toBeNull();
    expect(tracks?.[0].languageCode).toBe('en');
  });

  test('returns null when no playerResponse block exists', () => {
    expect(extractTracksFromHTML('<html></html>')).toBeNull();
  });

  test('returns null when captionTracks is empty', () => {
    const html =
      '<script>var ytInitialPlayerResponse = {"captions":{"playerCaptionsTracklistRenderer":{"captionTracks":[]}}};</script>';
    expect(extractTracksFromHTML(html)).toBeNull();
  });

  test('returns null when JSON is malformed', () => {
    const html = '<script>var ytInitialPlayerResponse = {bad json};</script>';
    expect(extractTracksFromHTML(html)).toBeNull();
  });
});

describe('selectCaptionTrack', () => {
  const tracks: CaptionTrack[] = [
    { baseUrl: 'u-auto-en', languageCode: 'en', kind: 'asr' },
    { baseUrl: 'u-manual-en', languageCode: 'en' },
    { baseUrl: 'u-manual-es', languageCode: 'es' },
  ];

  test('prefers manual over auto-generated for the same language', () => {
    const selected = selectCaptionTrack(tracks, ['en'], true);
    expect(selected?.baseUrl).toBe('u-manual-en');
  });

  test('honors language order', () => {
    const selected = selectCaptionTrack(tracks, ['es', 'en'], true);
    expect(selected?.languageCode).toBe('es');
  });

  test('excludes auto when includeAutoGenerated is false', () => {
    const onlyAuto: CaptionTrack[] = [{ baseUrl: 'u', languageCode: 'en', kind: 'asr' }];
    expect(selectCaptionTrack(onlyAuto, ['en'], false)).toBeNull();
  });

  test('falls back to first available track when no language matches', () => {
    const selected = selectCaptionTrack(tracks, ['fr'], true);
    expect(selected?.baseUrl).toBe('u-manual-en');
  });

  test('matches language prefix (en matches en-US)', () => {
    const localized: CaptionTrack[] = [{ baseUrl: 'u', languageCode: 'en-US' }];
    expect(selectCaptionTrack(localized, ['en'], true)?.languageCode).toBe('en-US');
  });

  test('returns null for empty track list', () => {
    expect(selectCaptionTrack([], ['en'], true)).toBeNull();
  });
});

describe('parseTimedTextEvents', () => {
  test('joins segs into a single string per event', () => {
    const segments = parseTimedTextEvents({
      events: [
        { segs: [{ utf8: 'Hello ' }, { utf8: 'world' }], tStartMs: 0, dDurationMs: 1500 },
      ],
    });
    expect(segments).toEqual([{ text: 'Hello world', start: 0, duration: 1.5 }]);
  });

  test('skips events without segs and empty segments', () => {
    const segments = parseTimedTextEvents({
      events: [
        { tStartMs: 0, dDurationMs: 1000 },
        { segs: [{ utf8: '   ' }], tStartMs: 1000, dDurationMs: 1000 },
        { segs: [{ utf8: 'kept' }], tStartMs: 2000, dDurationMs: 1000 },
      ],
    });
    expect(segments).toHaveLength(1);
    expect(segments[0].text).toBe('kept');
  });

  test('handles empty/missing events array', () => {
    expect(parseTimedTextEvents({})).toEqual([]);
    expect(parseTimedTextEvents({ events: [] })).toEqual([]);
  });

  test('handles missing timing fields gracefully', () => {
    const [seg] = parseTimedTextEvents({ events: [{ segs: [{ utf8: 'no time' }] }] });
    expect(seg).toEqual({ text: 'no time', start: 0, duration: 0 });
  });
});

