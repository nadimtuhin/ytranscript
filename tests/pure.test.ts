describe('extractIdFromJsonlRecord', () => {
  test('reads videoId from meta', () => {
    expect(extractIdFromJsonlRecord('{"meta":{"videoId":"abc"}}')).toBe('abc');
  });

  test('reads top-level videoId (legacy shape)', () => {
    expect(extractIdFromJsonlRecord('{"videoId":"xyz"}')).toBe('xyz');
  });

  test('prefers meta.videoId over top-level when both exist', () => {
    expect(extractIdFromJsonlRecord('{"videoId":"top","meta":{"videoId":"nested"}}')).toBe('nested');
  });

  test('returns null for malformed JSON', () => {
    expect(extractIdFromJsonlRecord('not json')).toBeNull();
  });

  test('returns null when record has no recognised id field', () => {
    expect(extractIdFromJsonlRecord('{"foo":"bar"}')).toBeNull();
  });
});

describe('extractVideoIdFromUrl (loose, history loader)', () => {
  test('extracts v param from youtube.com URL', () => {
    expect(extractVideoIdFromUrl('https://www.youtube.com/watch?v=dQw4w9WgXcQ')).toBe(
      'dQw4w9WgXcQ'
    );
  });

  test('extracts path from youtu.be', () => {
    expect(extractVideoIdFromUrl('https://youtu.be/abc')).toBe('abc');
  });

  test('returns null for non-YouTube URL', () => {
    expect(extractVideoIdFromUrl('https://example.com/?v=x')).toBeNull();
  });

  test('returns null for invalid URL', () => {
    expect(extractVideoIdFromUrl('not-a-url')).toBeNull();
  });

  test('does NOT validate length (loose semantics)', () => {
    // history loader keeps whatever YouTube put in `v=`
    expect(extractVideoIdFromUrl('https://www.youtube.com/watch?v=short')).toBe('short');
  });
});

describe('parseCSVLine', () => {
  test('splits on commas', () => {
    expect(parseCSVLine('a,b,c')).toEqual(['a', 'b', 'c']);
  });

  test('handles quoted values containing commas', () => {
    expect(parseCSVLine('"a,b",c')).toEqual(['a,b', 'c']);
  });

  test('handles escaped quotes ""', () => {
    expect(parseCSVLine('"he said ""hi""",end')).toEqual(['he said "hi"', 'end']);
  });

  test('trims whitespace around values', () => {
    expect(parseCSVLine('  a , b ,c')).toEqual(['a', 'b', 'c']);
  });
});

describe('parseCSV', () => {
  test('returns empty when fewer than two lines', () => {
    expect(parseCSV('')).toEqual([]);
    expect(parseCSV('only-header')).toEqual([]);
  });

  test('maps values to header keys', () => {
    const rows = parseCSV('id,name\n1,Alice\n2,Bob');
    expect(rows).toEqual([
      { id: '1', name: 'Alice' },
      { id: '2', name: 'Bob' },
    ]);
  });

  test('fills missing trailing cells with empty string', () => {
    const rows = parseCSV('a,b,c\nx,y');
    expect(rows[0]).toEqual({ a: 'x', b: 'y', c: '' });
  });
});

