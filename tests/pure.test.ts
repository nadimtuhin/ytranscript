describe('formatTimestamp', () => {
  test('produces a short M:SS format', () => {
    expect(formatTimestamp(0)).toBe('0:00');
    expect(formatTimestamp(65)).toBe('1:05');
    expect(formatTimestamp(3600)).toBe('60:00');
  });
});

describe('escapeCsvCell', () => {
  test('wraps value in quotes', () => {
    expect(escapeCsvCell('hello')).toBe('"hello"');
  });

  test('doubles embedded quotes', () => {
    expect(escapeCsvCell('say "hi"')).toBe('"say ""hi"""');
  });

  test('handles null/undefined as empty string', () => {
    expect(escapeCsvCell(null)).toBe('""');
    expect(escapeCsvCell(undefined)).toBe('""');
  });

  test('coerces non-strings via String()', () => {
    expect(escapeCsvCell(42)).toBe('"42"');
    expect(escapeCsvCell(true)).toBe('"true"');
  });
});

describe('rowToCsv', () => {
  test('joins escaped cells with commas', () => {
    expect(rowToCsv(['a', 'b', 'c'])).toBe('"a","b","c"');
  });

  test('escapes quotes per cell', () => {
    expect(rowToCsv(['ok', 'q"u"ote'])).toBe('"ok","q""u""ote"');
  });
});

describe('resultToCsvRow', () => {
  test('extracts successful row', () => {
    const row = resultToCsvRow({
      meta: {
        videoId: 'v1',
        source: 'manual',
        title: 'T',
        url: 'U',
        channel: { name: 'C' },
        watchedAt: 'W',
      },
      transcript: {
        videoId: 'v1',
        text: 'hello',
        language: 'en',
        isAutoGenerated: true,
        segments: [],
      },
    });
    expect(row).toEqual(['v1', 'manual', 'T', 'U', 'C', 'W', 'en', 'true', 'hello', '']);
  });

  test('extracts failure row with error', () => {
    const row = resultToCsvRow({
      meta: { videoId: 'v2', source: 'history' },
      transcript: null,
      error: 'boom',
    });
    expect(row).toEqual(['v2', 'history', '', '', '', '', '', 'false', '', 'boom']);
  });
});

