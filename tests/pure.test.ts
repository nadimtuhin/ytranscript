describe('chunk', () => {
  test('splits into batches of the given size', () => {
    expect(chunk([1, 2, 3, 4, 5], 2)).toEqual([[1, 2], [3, 4], [5]]);
  });

  test('returns a single batch when size exceeds length', () => {
    expect(chunk([1, 2], 10)).toEqual([[1, 2]]);
  });

  test('handles empty input', () => {
    expect(chunk([], 3)).toEqual([]);
  });

  test('size <= 0 returns the input as a single batch', () => {
    expect(chunk([1, 2, 3], 0)).toEqual([[1, 2, 3]]);
  });
});

describe('selectUnprocessed', () => {
  const videos: WatchHistoryMeta[] = [
    { videoId: 'a', source: 'manual' },
    { videoId: 'b', source: 'manual' },
    { videoId: 'c', source: 'manual' },
  ];

  test('filters out IDs already in the skip set', () => {
    const result = selectUnprocessed(videos, new Set(['a', 'c']));
    expect(result.map((v) => v.videoId)).toEqual(['b']);
  });

  test('returns the original array when skip set is empty', () => {
    expect(selectUnprocessed(videos, new Set())).toBe(videos);
  });

  test('returns empty when everything is skipped', () => {
    expect(selectUnprocessed(videos, new Set(['a', 'b', 'c']))).toEqual([]);
  });
});

